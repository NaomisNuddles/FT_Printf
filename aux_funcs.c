#include "ft_printf.h"

void	putlongnbr(long int n)
{
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", 1);
	else if (n < 0)
	{
		ft_putchar_fd(45, 1);
		n *= -1;
	}
	if (n > 9)
		putlongnbr(n / 10);
	if (n != -2147483648)
		ft_putchar_fd("0123456789"[n % 10], 1);
}